# ================= 章节生成完整提示词构建 =================
def build_chapter_prompt(
    doc_type: str,
    chapter_name: str,
    product_name: str,
    product_type: str,
    product_params: str = "",
    reference_content: str = "",
    web_content: str = ""
) -> str:
    """
    构建完整的章节生成提示词

    Args:
        doc_type: 文档类型
        chapter_name: 章节名称
        product_name: 产品名称
        product_type: 产品类型
        product_params: 产品参数
        reference_content: RAG检索的参考内容
        web_content: 网络搜索的内容

    Returns:
        完整的提示词字符串
    """

    prompt_parts = [SYSTEM_PROMPT]

    # 添加产品背景知识
    prompt_parts.append(PRODUCT_CONTEXT)

    # 添加文档类型特定要求
    if doc_type in DOC_TYPE_SPECIFIC_PROMPTS:
        prompt_parts.append(DOC_TYPE_SPECIFIC_PROMPTS[doc_type])

    # 添加参考内容
    if reference_content:
        prompt_parts.append(f"""【参考内容】
以下是从知识库检索到的相关参考文档，请严格参照这些内容的详细程度、格式和专业水平来生成新文档：

{reference_content}
""")

    # 添加网络搜索内容
    if web_content:
        prompt_parts.append(f"""【网络参考信息】
以下是从网络搜索到的相关法规标准信息，供参考：

{web_content}
""")

    # 添加产品信息和任务
    prompt_parts.append(f"""【产品信息】
- 产品名称：{product_name}
- 产品类型：{product_type}
- 产品参数：{product_params if product_params else "贴敷式胰岛素泵，闭环/半闭环控制，BLE通信，EO灭菌，IPX4防水"}

【任务】
请根据以上信息，为{product_name}的{get_doc_type_name(doc_type)}生成"【{chapter_name}】"章节内容。

【详细程度要求】
- 章节内容要极其详细和充实，不能是简单的框架或概述
- 每个要点都要有具体的阐述和说明
- 要有实际的数据、参数、例子支撑
- 要像正式的注册申报文档一样详细和专业
- 针对贴敷式胰岛素泵产品，包含该产品的具体特性和参数

{QUALITY_CHECK_PROMPT}

请开始生成内容：
""")

    return "\n" + "="*70 + "\n".join(prompt_parts)


def get_doc_type_name(doc_type: str) -> str:
    """获取文档类型的中文名称"""
    from app.services.doc_types import DOC_TYPE_LABELS
    return DOC_TYPE_LABELS.get(doc_type, doc_type)
