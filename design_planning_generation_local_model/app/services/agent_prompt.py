# ── System Prompt 构建函数 ──

def build_system_prompt(state: AgentState) -> str:
    """根据当前状态构建完整的Agent System Prompt

    对应PRD Section 6.1的5段结构:
    Section 1 (角色) + Section 2 (SOP) + Section 3 (工具规则)
    + Section 4 (回复风格) + Section 5 (动态状态快照)

    Args:
        state: 当前Agent状态

    Returns:
        完整的System Prompt字符串
    """
    # 构建状态快照JSON
    snapshot = build_state_snapshot(state)
    snapshot_json = json.dumps(snapshot, ensure_ascii=False, indent=2)

    # 附件摘要
    attachments = state.get("attachments", [])
    attachment_info = ""
    if attachments:
        filenames = [a.get("filename", "?") for a in attachments]
        total_chars = sum(a.get("char_count", 0) for a in attachments)
        attachment_info = f"""
## 用户已上传附件
{len(attachments)} 个文件, 共 {total_chars} 字符: {', '.join(filenames)}
- 使用 search_attachment 工具搜索附件中的具体内容
- 附件内容可在对话中引用，帮助用户分析或编写文档
"""

    state_section = f"""# 当前会话状态

以下是当前项目的实时状态。你的每次回复都应基于这份状态——知道进度在哪、什么已完成、什么待处理。

```json
{snapshot_json}
```
{attachment_info}
状态字段说明:
- status 取值: "not_started" | "in_progress" | "confirmed" | "partial" | "completed"
- unresolved_items: 用户跳过或待补充的项目
- 你的每次回复后，状态会自动更新
"""

    # 组装完整Prompt
    return (
        ROLE_DEFINITION + "\n"
        + SOP_KNOWLEDGE + "\n"
        + TOOL_RULES + "\n"
        + REPLY_STYLE + "\n"
        + state_section
    )
